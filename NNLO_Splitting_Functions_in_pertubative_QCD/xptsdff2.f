       PARAMETER ( N1 = -1, N2 = 1, NW = 2 )
       DIMENSION HC1(N1:N2),HC2(N1:N2,N1:N2),HC3(N1:N2,N1:N2,N1:N2),
     ,           HC4(N1:N2,N1:N2,N1:N2,N1:N2)
       DIMENSION HR1(N1:N2),HR2(N1:N2,N1:N2),HR3(N1:N2,N1:N2,N1:N2),
     ,           HR4(N1:N2,N1:N2,N1:N2,N1:N2)
       DIMENSION HI1(N1:N2),HI2(N1:N2,N1:N2),HI3(N1:N2,N1:N2,N1:N2),
     ,           HI4(N1:N2,N1:N2,N1:N2,N1:N2)
*
* ...Colour factors and abbreviations
*
       CF  = 4./3.D0
       CA  = 3.D0
*
       DX = 1.D0/X
       DM = 1.D0/(1.D0-X)
*
* ...The harmonic polylogs up to weight 2
*
       CALL HPLOG (X, NW, HC1,HC2,HC3,HC4, HR1,HR2,HR3,HR4,
     ,            HI1,HI2,HI3,HI4, N1, N2)
*
* ...The splitting function in terms of the harmonic polylogs
*
      pgg1d =
     &  + ca**2 * (  - 32.D0/3.D0*Hr1(0) - 56.D0/3.D0*Hr1(0)*x + 88.D0/
     &    3.D0*Hr1(0)*x**2 - 88.D0/3.D0*Hr1(0)*dx + 88.D0/3.D0*Hr1(0)*
     &    dm - 96.D0*Hr2(0,0)*x + 32.D0*Hr2(0,0)*x**2 - 32.D0*Hr2(0,0)*
     &    dx - 32.D0*Hr2(0,0)*dm + 64.D0*Hr2(0,1) - 32.D0*Hr2(0,1)*x +
     &    32.D0*Hr2(0,1)*x**2 - 32.D0*Hr2(0,1)*dx - 32.D0*Hr2(0,1)*dm
     &     + 64.D0*Hr2(1,0) - 32.D0*Hr2(1,0)*x + 32.D0*Hr2(1,0)*x**2 -
     &    32.D0*Hr2(1,0)*dx - 32.D0*Hr2(1,0)*dm )
      pgg1d = pgg1d + nf*ca * ( 32.D0/3.D0*Hr1(0) - 16.D0/3.D0*Hr1(0)*x
     &     + 16.D0/3.D0*Hr1(0)*x**2 - 16.D0/3.D0*Hr1(0)*dx - 16.D0/3.D0
     &    *Hr1(0)*dm )
      pgg1d = pgg1d + nf*cf * ( 24.D0 + 8.D0*x - 448.D0/9.D0*x**2 + 160.
     &    D0/9.D0*dx + 32.D0*Hr1(0) + 48.D0*Hr1(0)*x + 32.D0/3.D0*Hr1(0
     &    )*x**2 + 32.D0/3.D0*Hr1(0)*dx + 16.D0*Hr2(0,0) + 16.D0*Hr2(0,
     &    0)*x )
*
       X1GGTS = pgg1d
*
       RETURN
       END
