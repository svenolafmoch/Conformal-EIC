*
* ..The differences between the time-like and space-like pure-singlet 
*    and gluon-gluon splitting functions for the evolution of the
*    fragmentation functions and the parton distributions, at second 
*    and third order in alpha_s/(4 pi) for the scale mu_f = mu_r.
* 
* ..The code uses the package of Gehrmann and Remiddi for the harmonic
*    polylogarithms published in hep-ph/0107173 = CPC 141 (2001) 296.
*
* ..Reference: S. Moch and A. Vogt, arXiv:0708.3899 [hep-ph] 
*
* ..These functions need to be combined with the space-like results of
*               A. Vogt, S. Moch and J. Vermaseren,
*               hep-ph/0404111 = Nucl. Phys. B691 (2004) 129
*
* =====================================================================
*
*
* ..The second-order (NLO) pure-singlet function P_ps^T(1) - P_ps^S(1) 
*
       FUNCTION X1PSTS (X, NF)
*
       IMPLICIT REAL*8 (A - Z)
       COMPLEX*16 HC1, HC2, HC3, HC4
       INTEGER NF, NF2, N1, N2, NW, I1, I2, I3, N
       PARAMETER ( N1 = -1, N2 = 1, NW = 2 )
       DIMENSION HC1(N1:N2),HC2(N1:N2,N1:N2),HC3(N1:N2,N1:N2,N1:N2),
     ,           HC4(N1:N2,N1:N2,N1:N2,N1:N2)
       DIMENSION HR1(N1:N2),HR2(N1:N2,N1:N2),HR3(N1:N2,N1:N2,N1:N2),
     ,           HR4(N1:N2,N1:N2,N1:N2,N1:N2)
       DIMENSION HI1(N1:N2),HI2(N1:N2,N1:N2),HI3(N1:N2,N1:N2,N1:N2),
     ,           HI4(N1:N2,N1:N2,N1:N2,N1:N2)
*
* ...Colour factor and an abbreviation
*
       CF = 4./3.D0
       DX = 1.D0/X
*
* ...The harmonic polylogs up to weight 2 by Gehrmann and Remiddi
*
       CALL HPLOG (X, NW, HC1,HC2,HC3,HC4, HR1,HR2,HR3,HR4,
     ,            HI1,HI2,HI3,HI4, N1, N2)
*
* ...The splitting function in terms of the harmonic polylogs
*
       X1PSTS =
     &  + nf*cf * (  - 24.D0 - 8.D0*x + 448.D0/9.D0*x**2 - 160.D0/9.D0*
     &    dx - 24.D0*Hr1(0) - 56.D0*Hr1(0)*x - 64.D0/3.D0*Hr1(0)*x**2
     &     + 16.D0*Hr2(0,0) + 16.D0*Hr2(0,0)*x )
*
       RETURN
       END
